function CastGGPred(spell, unit)
	if spell == _Q then
		local QPrediction = GGPrediction:SpellPrediction({ Type = GGPrediction.SPELLTYPE_LINE, Delay = 0.25, Radius = 70, Range = 1300, Speed = 1200, Collision = true, CollisionTypes = { GGPrediction.COLLISION_MINION } })
		QPrediction:GetPrediction(unit, myHero)
		if QPrediction:CanHit(Menu.Pred.PredQ:Value() + 1) then
			Control.CastSpell(HK_Q, QPrediction.CastPosition)
		end
	else
		if spell == _W then
			local WPrediction = GGPrediction:SpellPrediction({ Type = GGPrediction.SPELLTYPE_CIRCLE, Delay = 0.25, Radius = 270, Range = 900, Speed =
			MathHuge, Collision = false })
			WPrediction:GetPrediction(unit, myHero)
			if WPrediction:CanHit(Menu.Pred.PredW:Value() + 1) then
				Control.CastSpell(HK_W, WPrediction.CastPosition)
			end
		end
	end
end
