		local type = Detected.type
		if type == "targeted" then
			if spell.target == myHero.handle then
				Control.CastSpell(HK_E, myHero)
				TableRemove(DetectedSpells, i)
			end
		else
			local startPos = Vector(spell.startPos); local placementPos = Vector(spell.placementPos); local unitPos =
			unit.pos
			local radius = Detected.radius; local range = Detected.range; local col = Detected.collision; local type =
			Detected.type
			local endPos, range2 = CalculateEndPos(startPos, placementPos, unitPos, range, radius, col, type)
			TableInsert(DetectedSpells,
				{ startPos = startPos, endPos = endPos, startTime = Game.Timer(), speed = Detected.speed, range = range2, delay =
				Detected.delay, radius = radius, radius2 = radius2 or nil, angle = angle or nil, type = type, collision =
